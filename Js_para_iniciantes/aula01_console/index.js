//Console.log é um método de imprimir os dados
//O ponto e virgula é opcional
//Envolver valores literais com aspas duplas, simples ou com crase
console.log('Andeson Silva');
console.log("Andeson Silva");
console.log(`Andeson Silva`);

//Para imprimir as aspas simples basta envolvê-la com duplas
console.log("Andeson 'Silva'");

//Para imprimir as aspas duplas basta envolvê-las com simples
console.log('Andeson "Silva"');

//Para imprimir aspas duplas e aspas simples ao mesmo tempo basta envolvê-las com crases
console.log(`'Andeson' "Silva"`);

//Em dados numéricos não existe a necessidade de colocar aspas e nem a crase
console.log(12345678910);

//O ponto é utilizado para representar as casas decimais
console.log(13.10);

//A vírgula serve para concatenar
console.log(25, 10.12, 'Andeson Silva');